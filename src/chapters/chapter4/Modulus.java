package chapters.chapter4;

public class Modulus {
    public static void main(String[] args) {
        int a = 20;
        int b = 21;
        double c = 21.125;

        System.out.println("a mod 2 = " + a % 2);
        System.out.println("b mod 2 = " + b % 2);
        System.out.println("c mod 2 = " + c % 2);
    }
}
